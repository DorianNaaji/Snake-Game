package Solidsnake.Model;

/*
Une énumération contenant les différentes directions : vers le haut, vers la droite, vers le bas et vers la gauche.
*/
public enum Direction {

    /**
     * Vers le haut.
     */
    HAUT,
    
    /**
     * Vers la droite.
     */
    DROITE,
    
    /**
     * Vers le bas.
     */
    BAS,
    
    /**
     * Vers la gauche.
     */
    GAUCHE;	
    
}
